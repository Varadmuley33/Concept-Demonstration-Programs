#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Structure Name : Demo
//  Description :    Contains different data members of various
//                   data types.
//
/////////////////////////////////////////////////////////////////

struct Demo
{
    int i;
    char ch1;
    float f;
    char ch2;
    int j;
};

/////////////////////////////////////////////////////////////////
//
//  Function Name : main
//  Description :   Entry point function of the application.
//  Input :         None
//  Output :        Integer
//
/////////////////////////////////////////////////////////////////

int main()
{
    /////////////////////////////////////////////////////////////////
    //
    //  Displays size of structure
    //
    /////////////////////////////////////////////////////////////////

    printf("Size of structure is : %lu\n", sizeof(struct Demo));

    return 0;
}
