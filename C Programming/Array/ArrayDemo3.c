#include<stdio.h>

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    int Arr[4] = {10, 20, 30, 40};          // Integer array

    printf("Arr : %lu\n", Arr);             // Base address of array
    printf("&Arr : %lu\n", &Arr);           // Address of whole array
    printf("&(Arr[0]) : %lu\n", &(Arr[0])); // Address of first element

    return 0;
}   // End of main

