//
/////////////////////////////////////////////////////////////////

class Demo
{
    public:
        void Display()
        {
            cout << "Inside display";
        }
};

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    Demo dobj;

    cout << sizeof(dobj);      // Displays size of empty class object

    return 0;
}

