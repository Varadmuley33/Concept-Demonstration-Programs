/////////////////////////////////////////////////////////////////
//
//  Required Header files
//
/////////////////////////////////////////////////////////////////

#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////
//
//  Namespace Name : PPA
//  Description    : Contains an integer variable.
//
/////////////////////////////////////////////////////////////////

namespace PPA
{
    int i = 11;
}

/////////////////////////////////////////////////////////////////
//
//  Namespace Name : LB
//  Description    : Contains an integer variable.
//
/////////////////////////////////////////////////////////////////

namespace LB
{
    int i = 21;
}

/////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
/////////////////////////////////////////////////////////////////

int main()
{
    cout << PPA::i << "\n";        // 11
    cout << LB::i << "\n";         // 21

    return 0;
}

/////////////////////////////////////////////////////////////////
//
//  Testcases:
//
//  Output :
//  11
//  21
//
/////////////////////////////////////////////////////////////////
